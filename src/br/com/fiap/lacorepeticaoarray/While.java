package br.com.fiap.lacorepeticaoarray;

public class While {
	
	public static void main(String[] args) {
		//metodo main é essencial para executar loops e outras coisas
		//Laço de repetição while de 1 a 10.
		int numero = 0;
		while(numero < 10) {
			numero = numero +1;
			System.out.println(numero);
		}
	}
}
